    private void showDSDichVu() {
        Connection conn = ConnectionOracle.getConn();
        String sql = "SELECT TENDV FROM DICHVU";
        //tbModel1.setRowCount(0);
        try {
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();

            while (rs.next()) {
                tbModel1.addRow(new String[]{rs.getString("TENDV")});
            }

            tbTenDichVu.setModel(tbModel1);
            tbTenDichVu.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(cau2_y3.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

