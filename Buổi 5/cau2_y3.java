    public String getMaKB() {
        Connection conn = ConnectionOracle.getConn();//dd-ora-yy
        String sql = "Select makb from khambenh where mabs = ? and mabn  = ? and ngaykham = ? and ketluan = ? and yeucaukham = ?";
        try {
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, getMaBS());
            statement.setString(2, getMaBN());
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MMM/yy");
            String date = sdf.format(jNgayKham.getDate());
            //System.out.println(date);
            statement.setString(3, date);
            statement.setString(4, txtKetLuan.getText());
            statement.setString(5, txtYeuCauKham.getText());
            ResultSet rs = statement.executeQuery();
            while (rs.next()) {
                return rs.getString("makb");
                //System.out.println(rs.getString("tenbs"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        return null;
    }


    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Connection conn = ConnectionOracle.getConn();//dd-ora-yy
        int sluong = tbDVBSChon.getRowCount();
        int sum = 0;
        String madv = null;
        int gia = 0;
        int sldv;
        // them vao thu phi
        for (int i = 0; i < sluong; i++) {
            String sql1 = "select madv, dongia from dichvu where tendv = ?";
            try {
                PreparedStatement statement1 = conn.prepareStatement(sql1);
                statement1.setString(1, (String) tbDVBSChon.getValueAt(i, 0));
                ResultSet rs1 = statement1.executeQuery();
                while (rs1.next()) {
                    madv = rs1.getString("madv");
                    gia = rs1.getInt("dongia");
                }
                if (!madv.isEmpty() && getMaKB() != null) {
                    String sql = "insert into thuphi (makb, madv, soluong, thanhtien) values(?, ?, ?, ?)";
                    PreparedStatement statement = conn.prepareStatement(sql);
                    statement.setString(1, getMaKB());
                    statement.setString(2, madv);
                    sldv = Integer.parseInt((String) tbDVBSChon.getValueAt(i, 1));
                    statement.setInt(3, sldv);
                    statement.setInt(4, gia * sldv);
                    sum += gia * sldv;
                    int check = statement.executeUpdate();
                    if (check > 0) {
                        JOptionPane.showMessageDialog(this, "Them thu phi thanh cong");
                    } else {
                        JOptionPane.showMessageDialog(this, "them thu phi that bai");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "them thu phi that bai");
                }

            } catch (SQLException ex) {
                Logger.getLogger(cau2_y3.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        // cap nhat kham benh
        if (sum > 0 && getMaKB() != null) {
            String sql2 = "update khambenh set ketluan = ? and thanhtoan = 0 where makb = ?";
            try {
                PreparedStatement statement2 = conn.prepareStatement(sql2);
                statement2.setString(1, txtKetLuan.getText());
                statement2.setString(2, getMaKB());
                int check = statement2.executeUpdate();
                if (check > 0) {
                    JOptionPane.showMessageDialog(this, "Cap nhat kham benh thanh cong");
                } else {
                    JOptionPane.showMessageDialog(this, "Cap nhat kham benh that bai");
                }
            } catch (SQLException ex) {
                Logger.getLogger(cau2_y3.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cap nhat kham benh that bai");
        }


    }//GEN-LAST:event_btnThemActionPerformed

