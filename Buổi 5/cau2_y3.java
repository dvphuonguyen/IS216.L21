    private void showDataBS() {
        ArrayList<String> data = new ArrayList();
        Connection conn = ConnectionOracle.getConn();//dd-ora-yy
        String sql = "Select tenbs from bacsi";
        try {
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();
            while (rs.next()) {
                data.add(rs.getString("tenbs"));
                //System.out.println(rs.getString("tenbs"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        for (String string : data) {
            cbBacSi.addItem(string);
        }
    }

    public String getTenBS() {
        return (String) cbBacSi.getSelectedItem();
    }

    public String getMaBS() {
        Connection conn = ConnectionOracle.getConn();
        String sql = "select mabs from bacsi where tenbs = ?";
        try {
            PreparedStatement statement = conn.prepareStatement(sql);
            String item = (String) cbBacSi.getSelectedItem();
            statement.setString(1, item);
            ResultSet rs = statement.executeQuery();
            while (rs.next()) {
                return rs.getString("mabs");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        return null;
    }

