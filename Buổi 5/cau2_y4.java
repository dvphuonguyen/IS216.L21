    private void txtMaBNKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMaBNKeyPressed
        // TODO add your handling code here:
        Connection conn = ConnectionOracle.getConn();//dd-ora-yy
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String sql = "select tenbn from benhnhan where mabn = ?";
            int count = 0;
            try {
                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, txtMaBN.getText());
                ResultSet rs = statement.executeQuery();
                while (rs.next()) {
                    txtTenBN.setText(rs.getString("tenbn"));
                    count += 1;
                }
                if (count == 0) {
                    JOptionPane.showMessageDialog(this, "Benh nhan chua dang ky");
                    reset();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_txtMaBNKeyPressed
