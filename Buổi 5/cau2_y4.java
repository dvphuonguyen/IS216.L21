    private void btnHienThiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHienThiActionPerformed
        // TODO add your handling code here:
        Connection conn = ConnectionOracle.getConn();
        String sql = "select makb, ketluan, yeucaukham,thanhtoan from khambenh where mabn = ? and ngaykham  = ?";
        try {
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, txtMaBN.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MMM/yy");
            String date = sdf.format(txtNgayKham.getDate());
            //System.out.println(date);
            statement.setString(2, date);
            ResultSet rs = statement.executeQuery();
            while (rs.next()) {
                makb = rs.getString("makb");
                txtKetLuan.setText(rs.getString("ketluan"));
                txtYeuCauKham.setText(rs.getString("yeucaukham"));
                //txtTongTien.setText(String.valueOf("thanhtoan"));
                thanhtoan = rs.getInt("thanhtoan");
            }
            
            tbModel1 = new DefaultTableModel();
            tbModel1.setColumnIdentifiers(new String[]{"Ten dich vu", "So luong", "Thanh tien"});
            String sql1 = "select tendv, soluong, thanhtien from thuphi tp join dichvu dv on tp.madv = dv.madv where makb = ?";
            PreparedStatement statement1 = conn.prepareStatement(sql);
            statement1.setString(1, makb);
            ResultSet rs1 = statement1.executeQuery();
            Object[] row = new Object[3];
            while(rs1.next()){
                row[0] = rs1.getString("TENDV");
                row[1] = rs1.getInt("soluong");
                row[2] = rs1.getInt("thanhtien");
                tbModel1.addRow(row);
            }
            tbDSDV.setModel(tbModel1);
            tbDSDV.setVisible(true);
            int sum = 0;
            for(int i = 0; i < tbDSDV.getRowCount(); i++){
                sum += Integer.parseInt((String) tbDSDV.getValueAt(i, 2));
            }
            
            txtTongTien.setText(String.valueOf(sum));
            
            if(thanhtoan == 1){
                JOptionPane.showMessageDialog(this, "Da thanh toan");
                cbDaThanhToan.setSelected(true);
                btnThanhToan.setEnabled(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
  
    }//GEN-LAST:event_btnHienThiActionPerformed

